package BasicsOfJava;

public class DataTypes {
        public static void main(String[] args) {
            boolean flag = true;
            int a = 322;
            short s = 112;
            byte b= 23;
            long l = 322424214;
            float f = 3344.32f;
            double d = 32333.3232;
            char  ch = 'c';
            System.out.println("The boolean variables is " +flag);
            System.out.println("The integer variables is " +a);
            System.out.println("The short variables is " +s);
            System.out.println("The byte variables is " +b);
            System.out.println("The long variables is " +l);
            System.out.println("The float variables is " +f);
            System.out.println("The double variables is " +d);
            System.out.println("The char variables is " +ch);

        }
    }


